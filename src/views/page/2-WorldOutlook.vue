                <li>一般来说，每种动物都有着自己独一无二的能力，例如狼族的奔跑、力量等方面非常强大。</li>
                <li>而龙族部落则为最为特殊的存在，他们的喉咙里含有一种名为【热腺】的东西，这种东西能将周围的空气吸入并且存储在热腺里面。由于龙天生为冷血动物，因此体内必然含有一个发热的器官，这个器官就是热腺。龙可以将气体吸入后，在热腺里面转换成硫化氢的形式，而热腺里面又含有一种产生高热量的【氧化钙】，在遇到水则大量放热，因此龙只需要将气体吸入，之后打开热腺口，便可将硫化氢气体喷出，并且高热量使得气体立刻燃烧起来，从而达到喷火的效果。</li>
                <li>幼年龙并没有热腺，只有青年龙【16岁以上】以上才有热腺。龙最长一次喷火记录，可以至少喷整整5分钟，范围之大可将一整栋大楼全部烧毁。记录产生的三天后，这个龙由于喷火太用力、太持久，从而导致了热腺癌，一个月后死去。因此龙在一般情况下不会随意喷火。喷一次火会消耗大量的精力，且极易导致非常不好的后果。会喷火的龙只有双翅龙和四足龙，其余的龙均无法喷火。但也被划分进龙族族谱里。</li>
                <li>龙族是整个世界里品种最少的一种兽人，他的品种只有【翼龙、四足龙、双翅龙、鲨龙】四种品种。我们的主角属于双翅龙的一种，即“手和翅膀是分开的”，翼龙则是手和翅膀合为一体的。鲨龙则是半水生的兽人，没有翅膀。四足龙顾名思义就是四只脚走路的龙，没有手。这四种品种的龙又催生出不同颜色的，例如双翅龙有蓝皮肤、红皮肤、黄皮肤，其他几种也都有不同颜色。几乎所有的龙都有龙角，这是一种名贵的药材。</li>
                <li>说道龙族的起源，承接上文：在蜥蜴族和鳄鱼族违反了禁律，进行了通婚，诞生了一种生物，它有着蜥蜴族般的脑袋，鳄鱼族的鳞甲与尾巴。这种生物原本是单独被划分成巨蜥族。但是由于时代变迁，巨蜥族不断壮大，他们原本非常适应不了环境，但是巨蜥族与鹰族通婚后产生了的龙，具有鹰般的力量，同时也进化出了翅膀。成为了现在的双翅龙。而鲨龙则有这鲨鱼般的鱼鳍，能在水中呼吸。四足龙则有着强力的四肢与背甲，是巨蜥族与鹿族通婚后产生的。而翼龙的手和翅膀是连在一起的，如同双足飞龙一般，是巨蜥族与蝠族的后代，这种生物能很好的适应环境，因此活了下来。</li>
                <li>在第二种说法出现以后，世界上物种多样性很快就上涨了，但是此时政府颁布了一条命令，任何生物绝对不允许通婚，一旦发现，格杀勿论，曾经通婚过的兽人将自成一个行政区，政府同意赋予其正常公民的权利。</li>
